// 配列の各要素を先頭から順に1, 2, 3, 4, 5で初期化して表示
#include <stdio.h>

int main(void)
{
    int i;
    int v[5] = {1, 2, 3, 4, 5};
    // int v[] = {1, 2, 3, 4, 5};  // 要素数は省略可（自動的に5とみなされる）
    // int v[] = {1, 3}            // {1, 3, 0, 0, 0}で初期化
    // int v[5] = {0};             // {0, 0, 0, 0, 0}で初期化

    for (i = 0; i < 5; i++) {
        printf("v[%d] = %d\n", i, v[i]);
    }
    return 0;
}
